async function main() {
  const contentDir = path.join(__dirname, 'content');
  const baseDate = new Date('2025-03-10T08:00:00Z');

  for (const a of articles) {
    const num = String(a.num).padStart(2, '0');
    const frFile = path.join(contentDir, `article-${num}-fr.html`);
    const enFile = path.join(contentDir, `article-${num}-en.html`);
    const arFile = path.join(contentDir, `article-${num}-ar.html`);

    if (!fs.existsSync(frFile)) {
      console.log(`⚠ Skipping article ${num}: FR content file not found`);
      continue;
    }

    const contentFr = fs.readFileSync(frFile, 'utf-8');
    const contentEn = fs.existsSync(enFile) ? fs.readFileSync(enFile, 'utf-8') : contentFr;
    const contentAr = fs.existsSync(arFile) ? fs.readFileSync(arFile, 'utf-8') : contentFr;
    const publishDate = new Date(baseDate.getTime() + a.publishOffset * 86400000);

    const existing = await prisma.blogPost.findUnique({ where: { slugFr: a.slug } });
    if (existing) {
      console.log(`⏭ Article ${num} already exists: ${a.slug}`);
      continue;
    }

    try {
      await prisma.blogPost.create({
        data: {
          slug: a.slug,
          slugFr: a.slug,
          slugEn: a.slugEn,
          slugAr: a.slugAr,
          coverImage: a.coverImage,
          featured: a.featured,
          titleFr: a.titleFr,
          titleEn: a.titleEn,
          titleAr: a.titleAr,
          metaTitleFr: a.metaTitleFr,
          metaTitleEn: a.metaTitleEn,
          metaTitleAr: a.metaTitleAr,
          metaDescFr: a.metaDescFr,
          metaDescEn: a.metaDescEn,
          metaDescAr: a.metaDescAr,
          contentFr: contentFr,
          contentEn: contentEn,
          contentAr: contentAr,
          publishedAt: publishDate,
          categories: {
            connect: a.categoryIds.map(id => ({ id }))
          }
        }
      });
      console.log(`✅ Article ${num}: ${a.titleFr}`);
    } catch (err) {
      console.error(`❌ Article ${num} failed:`, err.message);
    }
  }
}

main()
  .then(() => { console.log('\n🎉 New articles seed complete!'); prisma.$disconnect(); })
  .catch(e => { console.error(e); prisma.$disconnect(); process.exit(1); });
